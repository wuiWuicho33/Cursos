/* Variable con un dato falso*/
var numeroFalso = "33.3";

/* ==========
Función Numer
=============
Convierte a un número fLotante o a uno decimal */
console.log(Number (numeroFalso) + 7);

/* =============
Función parseInt
================
Convierte a un número entero */
console.log(parseInt (numeroFalso) + 7);

/* ===============
Función parseFloat
==================
Convierte a un número decimal */
console.log(parseFloat (numeroFalso) + 7);

