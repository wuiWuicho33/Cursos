/*Los operadores aritméticos básicos son (+, - , * , / , % )*/
var numero1 = 5;
var numero2 = 5;
var sumar = numero1 + numero2;
var restar = numero1 - numero2;
var multiplicar = numero1 * numero2;
var dividir = numero1 / numero2;
var porcentaje = numero1 % numero2;

document.getElementById("demo-01").innerHTML="5 + 5 = " + sumar;
document.getElementById("demo-02").innerHTML="5 - 5 = " + restar;
document.getElementById("demo-03").innerHTML="5 * 5 = " + multiplicar;
document.getElementById("demo-04").innerHTML="5 / 5 = " + dividir;
document.getElementById("demo-05").innerHTML="5 % 5 = " + porcentaje;


